package umVariaveis;

public class Variaveis {

	public static void main(String[] args) {
		
		int numeroInteiro = 3;
		double numeroQuebrado = 1.5;
		boolean verdadeiroFalso = false;
				
		numeroInteiro = 10;
		System.out.println("O numero é: " +numeroInteiro);
		
		numeroInteiro = 20;
		
	
		System.out.println("A variavel do tipo inteiro pode receber " +numeroInteiro);
		System.out.println("A variavel do tipo double é " +numeroQuebrado);
		System.out.println("Variavel do tipo boolean pode receber " +verdadeiroFalso);
	}
}
